def role_onboarding_keyboard(*, role: str, step: int, target_tg_id: int) -> InlineKeyboardMarkup:
    total = len(ROLE_ONBOARDING_TEXTS[role])
    next_text = "✅ Завершить обучение" if step >= total else "➡️"
    next_action = "finish" if step >= total else "next"
    return InlineKeyboardMarkup(
        inline_keyboard=[
            [
                InlineKeyboardButton(text="⬅️", callback_data=f"role_onb:prev:{role}:{step}:{target_tg_id}"),
                InlineKeyboardButton(text=next_text, callback_data=f"role_onb:{next_action}:{role}:{step}:{target_tg_id}"),
            ],
            [InlineKeyboardButton(text="⏭ Пропустить обучение", callback_data=f"role_onb:skip:{role}:{step}:{target_tg_id}")],
        ]
    )


def format_step_text(role: str, step: int) -> str:
    total = len(ROLE_ONBOARDING_TEXTS[role])
    return f"{step}/{total}\n\n{ROLE_ONBOARDING_TEXTS[role][step - 1]}"


async def maybe_start_role_onboarding(bot: Bot, target_tg_id: int, role: str) -> bool:
    role_key = normalize_onboarding_role_key(role)
    if role_key not in ROLE_ONBOARDING_TEXTS:
        return False
    row = await get_role_onboarding(target_tg_id, role_key)
    if row and row.get("status") in {"completed", "skipped"}:
        logger.info("onboarding_not_started_already_done target=%s role=%s status=%s", target_tg_id, role_key, row.get("status"))
        return False
    await upsert_onboarding_progress(target_tg_id, role_key, 1)
    logger.info("onboarding_created target=%s role=%s status=in_progress step=1", target_tg_id, role_key)
    await bot.send_message(target_tg_id, format_step_text(role_key, 1), reply_markup=role_onboarding_keyboard(role=role_key, step=1, target_tg_id=target_tg_id))
    return True


async def mark_step(role: str, target_tg_id: int, step: int) -> None:
    await upsert_onboarding_progress(target_tg_id, role, step)


async def finish_onboarding(role: str, target_tg_id: int) -> None:
    await upsert_onboarding_completed(target_tg_id, role)


async def skip_onboarding(role: str, target_tg_id: int) -> None:
    await upsert_onboarding_skipped(target_tg_id, role)
